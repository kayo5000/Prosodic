"""
train_rhyme_model.py

Training script for the Prosodic RhymeSiameseNet.

Loads labeled phoneme pair data from training_data.json (generated by
data_generator.py), trains the Siamese BiGRU network for 50 epochs,
and saves the best model to models/rhyme_model.pt.

Usage:
    python train_rhyme_model.py

Prerequisites:
    1. pip install torch nltk  (see Step 14 for full pip install list)
    2. python data_generator.py  (generates training_data.json, ~5-10 min)
    3. python train_rhyme_model.py  (this script, ~20-40 min on i9 CPU)

Estimated training time: 20–40 min on i9 CPU (no GPU required).
GPU will be used automatically if available (CUDA or MPS).

Output:
    models/rhyme_model.pt  — best checkpoint by validation loss
    prosodic_errors.log    — any errors encountered during training
"""

