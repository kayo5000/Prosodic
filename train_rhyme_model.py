def run_post_training_validation(
    model: RhymeSiameseNet,
    vocab: PhonemeVocab,
    device: torch.device,
) -> None:
    """
    Run 10 hard-coded validation pairs and print PASS/FAIL for each.

    A pair PASSES if the predicted score falls within [min_score, max_score].
    A pair FAILS if the score is outside that range.

    Prints a summary with pass count and overall PASS/FAIL verdict.

    Args:
        model:  Trained RhymeSiameseNet.
        vocab:  PhonemeVocab.
        device: Compute device.
    """
    print("\n" + "=" * 68)
    print("POST-TRAINING VALIDATION")
    print("=" * 68)
    print(f"{'Word A':<12} {'Word B':<12} {'Expected':<8} {'Score':>7}  {'Range':<16} {'Result'}")
    print("-" * 68)

    passed = 0
    total = len(VALIDATION_PAIRS)

    model.eval()
    with torch.no_grad():
        for vp in VALIDATION_PAIRS:
            score = model.score(
                vp["phonemes_a"],
                vp["phonemes_b"],
                vocab=vocab,
                device=device,
            )
            lo = vp["min_score"]
            hi = vp["max_score"]
            result = "PASS" if lo <= score <= hi else "FAIL"
            if result == "PASS":
                passed += 1

            range_str = f"[{lo:.2f} – {hi:.2f}]"
            print(
                f"{vp['word_a']:<12} {vp['word_b']:<12} "
                f"{vp['expected']:<8} {score:>7.4f}  "
                f"{range_str:<16} {result}  ({vp['note']})"
            )

    print("-" * 68)
    print(f"Result: {passed}/{total} PASS")
    if passed == total:
        print("Overall: PASS — model is ready for use")
    elif passed >= total * 0.8:
        print(f"Overall: PARTIAL PASS — {total - passed} pair(s) outside expected range")
        print("Consider retraining with more epochs or checking training data quality.")
    else:
        print(f"Overall: FAIL — {total - passed} pair(s) failed")
        print("Training may not have converged. Check loss curve and training data.")
    print("=" * 68)


