def load_training_data(path: str) -> List[dict]:
    """
    Load and validate training pairs from training_data.json.

    Args:
        path: Path to training_data.json produced by data_generator.py.

    Returns:
        List of pair dicts. Exits with error message if file not found.
    """
    if not os.path.isfile(path):
        print(f"\nERROR: {path} not found.")
        print("Run: python data_generator.py  (takes 5–10 min)")
        sys.exit(1)

    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)

    pairs = data.get("pairs", [])
    if not pairs:
        print(f"ERROR: No pairs found in {path}")
        sys.exit(1)

    # Filter out pairs with missing phoneme data
    valid = [
        p for p in pairs
        if p.get("phonemes_a") and p.get("phonemes_b")
    ]
    skipped = len(pairs) - len(valid)
    if skipped:
        logger.warning("Skipped %d pairs with missing phoneme data", skipped)

    return valid


def split_data(
    pairs: List[dict],
    train_ratio: float = TRAIN_SPLIT,
    seed: int = RANDOM_SEED,
) -> Tuple[List[dict], List[dict]]:
    """
    Shuffle and split pairs into train/val sets, stratified by label.

    Stratification ensures each split has the same label distribution,
    preventing a train set dominated by one rhyme type.

    Args:
        pairs:       Full list of pair dicts.
        train_ratio: Fraction of data for training (default 0.80).
        seed:        Random seed for reproducibility.

    Returns:
        Tuple of (train_pairs, val_pairs).
    """
    rng = random.Random(seed)

    # Group by label
    by_label: Dict[str, List[dict]] = {}
    for p in pairs:
        lbl = p.get("label", "none")
        by_label.setdefault(lbl, []).append(p)

    train, val = [], []
    for lbl, group in by_label.items():
        rng.shuffle(group)
        split_idx = int(len(group) * train_ratio)
        train.extend(group[:split_idx])
        val.extend(group[split_idx:])

    rng.shuffle(train)
    rng.shuffle(val)

    return train, val


