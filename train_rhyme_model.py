def _get_device() -> torch.device:
    """
    Select the best available compute device.

    Prefers CUDA, then MPS (Apple Silicon), then CPU.
    Prints selection so the user knows which device is active.
    """
    if torch.cuda.is_available():
        device = torch.device("cuda")
        print(f"  Device: CUDA ({torch.cuda.get_device_name(0)})")
    elif hasattr(torch.backends, "mps") and torch.backends.mps.is_available():
        device = torch.device("mps")
        print("  Device: Apple MPS")
    else:
        device = torch.device("cpu")
        print("  Device: CPU")
    return device


def _print_label_distribution(name: str, pairs: List[dict]) -> None:
    """Print the label distribution of a dataset split to stdout."""
    counts: Dict[str, int] = {}
    for p in pairs:
        lbl = p.get("label", "none")
        counts[lbl] = counts.get(lbl, 0) + 1
    print(f"  {name}: {len(pairs):,} pairs")
    for lbl in ["perfect", "slant", "near", "none"]:
        print(f"    {lbl:<10} {counts.get(lbl, 0):>6,}")


def _run_epoch(
    model: RhymeSiameseNet,
    loader: DataLoader,
    loss_fn: ContrastiveLoss,
    optimizer: Optional[torch.optim.Optimizer],
    device: torch.device,
    training: bool,
) -> Tuple[float, List[Tuple[str, str, float, float]]]:
    """
    Run one full pass over a DataLoader (train or eval).

    Args:
        model:     RhymeSiameseNet instance.
        loader:    DataLoader for train or val set.
        loss_fn:   ContrastiveLoss.
        optimizer: Adam optimizer (None during eval).
        device:    Compute device.
        training:  True for train mode, False for eval mode.

    Returns:
        Tuple of (mean_loss, sample_pairs) where sample_pairs is a list
        of up to 5 (word_a, word_b, predicted_score, true_label) tuples
        collected from the last batch, for progress printing.
    """
    model.train(training)
    context = torch.enable_grad() if training else torch.no_grad()
    total_loss = 0.0
    n_batches = 0
    sample_pairs: List[Tuple[str, str, float, float]] = []

    with context:
        for batch in loader:
            idx_a, str_a, idx_b, str_b, labels = [t.to(device) for t in batch]

            scores = model(idx_a, str_a, idx_b, str_b)
            loss = loss_fn(scores, labels)

            if training and optimizer is not None:
                optimizer.zero_grad()
                loss.backward()
                # Gradient clipping — prevents exploding gradients on short sequences
                nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
                optimizer.step()

            total_loss += loss.item()
            n_batches += 1

    mean_loss = total_loss / max(n_batches, 1)
    return mean_loss, sample_pairs


def _collect_sample_pairs(
    model: RhymeSiameseNet,
    val_pairs: List[dict],
    vocab: PhonemeVocab,
    device: torch.device,
    n: int = 5,
    seed: int = 0,
) -> None:
    """
    Print n random sample predictions from the val set.

    Shows: word_a | word_b | true_label | predicted_score
    Called every 10 epochs to show learning progress.

    Args:
        model:     Model in eval mode.
        val_pairs: Validation pair list.
        vocab:     PhonemeVocab.
        device:    Compute device.
        n:         Number of samples to print.
        seed:      Random seed for sample selection.
    """
    rng = random.Random(seed)
    samples = rng.sample(val_pairs, min(n, len(val_pairs)))

    model.eval()
    with torch.no_grad():
        for p in samples:
            ph_a = p.get("phonemes_a", [])
            ph_b = p.get("phonemes_b", [])
            true_score = p.get("similarity_score", 0.0)
            label = p.get("label", "?")

            score = model.score(ph_a, ph_b, vocab=vocab, device=device)
            wa = p.get("word_a", "?")
            wb = p.get("word_b", "?")
            print(f"    {wa:<14} {wb:<14} label={label:<8} true={true_score:.2f}  pred={score:.4f}")


