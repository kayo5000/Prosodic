def train() -> None:
    """
    Full training pipeline for RhymeSiameseNet.

    Steps:
        1. Load training_data.json
        2. 80/20 stratified train/val split
        3. Build DataLoaders
        4. Initialize model, Adam optimizer, ContrastiveLoss
        5. Train for 50 epochs, print loss every epoch
        6. Print 5 sample predictions every 10 epochs
        7. Save best model to models/rhyme_model.pt (lowest val loss)
        8. Post-training validation on 10 benchmark pairs
    """
    print("=" * 68)
    print("PROSODIC — RhymeSiameseNet Training")
    print("Estimated time: 20–40 min on i9 CPU")
    print("=" * 68)

    # --- Device ---
    device = _get_device()

    # --- Data ---
    print("\n[1/4] Loading training data...")
    pairs = load_training_data(TRAINING_DATA_PATH)
    print(f"  Loaded {len(pairs):,} valid pairs from {TRAINING_DATA_PATH}")

    train_pairs, val_pairs = split_data(pairs)
    print(f"\n  Split: {len(train_pairs):,} train / {len(val_pairs):,} val (80/20 stratified)")
    _print_label_distribution("Train", train_pairs)
    _print_label_distribution("Val  ", val_pairs)

    train_ds = RhymePairDataset(train_pairs, VOCAB)
    val_ds   = RhymePairDataset(val_pairs, VOCAB)

    train_loader = DataLoader(
        train_ds,
        batch_size=BATCH_SIZE,
        shuffle=True,
        num_workers=0,   # 0 for Windows compatibility
        pin_memory=(device.type == "cuda"),
    )
    val_loader = DataLoader(
        val_ds,
        batch_size=BATCH_SIZE,
        shuffle=False,
        num_workers=0,
    )

    # --- Model, optimizer, loss ---
    print("\n[2/4] Initializing model...")
    model = RhymeSiameseNet().to(device)
    total_params = sum(p.numel() for p in model.parameters())
    print(f"  RhymeSiameseNet: {total_params:,} parameters")

    optimizer = torch.optim.Adam(model.parameters(), lr=LEARNING_RATE)

    # Learning rate scheduler: halve LR if val loss plateaus for 5 epochs
    scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(
        optimizer, mode="min", factor=0.5, patience=5, verbose=True
    )

    loss_fn = ContrastiveLoss(margin=CONTRASTIVE_MARGIN)

    # --- Training loop ---
    print(f"\n[3/4] Training for {NUM_EPOCHS} epochs...")
    print(f"  Batch size: {BATCH_SIZE}, LR: {LEARNING_RATE}, Margin: {CONTRASTIVE_MARGIN}")
    print("-" * 68)
    print(f"{'Epoch':>5}  {'Train Loss':>12}  {'Val Loss':>10}  {'Best':>6}  {'LR':>10}")
    print("-" * 68)

    best_val_loss = float("inf")
    best_epoch = 0

    for epoch in range(1, NUM_EPOCHS + 1):
        # Train
        train_loss, _ = _run_epoch(
            model, train_loader, loss_fn, optimizer, device, training=True
        )

        # Validate
        val_loss, _ = _run_epoch(
            model, val_loader, loss_fn, None, device, training=False
        )

        # LR schedule step
        scheduler.step(val_loss)
        current_lr = optimizer.param_groups[0]["lr"]

        # Track best model
        is_best = val_loss < best_val_loss
        if is_best:
            best_val_loss = val_loss
            best_epoch = epoch
            os.makedirs(os.path.dirname(MODEL_OUTPUT_PATH), exist_ok=True)
            save_model(
                model,
                MODEL_OUTPUT_PATH,
                metadata={
                    "epoch": epoch,
                    "train_loss": train_loss,
                    "val_loss": val_loss,
                    "best": True,
                },
            )

        best_marker = "*" if is_best else " "
        print(
            f"{epoch:>5}  {train_loss:>12.6f}  {val_loss:>10.6f}  "
            f"{best_marker:>6}  {current_lr:>10.6f}"
        )

        # Sample predictions every 10 epochs
        if epoch % 10 == 0:
            print(f"\n  --- Sample predictions at epoch {epoch} ---")
            _collect_sample_pairs(model, val_pairs, VOCAB, device, n=5, seed=epoch)
            print()

    print("-" * 68)
    print(f"\n  Best model: epoch {best_epoch}, val_loss={best_val_loss:.6f}")
    print(f"  Saved to: {MODEL_OUTPUT_PATH}")

    # --- Load best model for validation ---
    print("\n[4/4] Loading best checkpoint for post-training validation...")
    best_model = load_model(MODEL_OUTPUT_PATH, device=device)

    # --- Post-training validation ---
    run_post_training_validation(best_model, VOCAB, device)


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    torch.manual_seed(RANDOM_SEED)
    random.seed(RANDOM_SEED)
    train()
