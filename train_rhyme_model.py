class RhymePairDataset(Dataset):
    """
    PyTorch Dataset wrapping labeled phoneme pairs from training_data.json.

    Each item returns encoded tensor pairs and a soft contrastive label.
    The similarity_score from the generator is used directly as the label
    so the loss can learn from partial rhyme strength, not just 0/1.

    Attributes:
        pairs:  List of pair dicts from training_data.json.
        vocab:  PhonemeVocab for encoding phoneme lists to indices.
    """

    def __init__(self, pairs: List[dict], vocab: PhonemeVocab) -> None:
        """
        Initialize dataset.

        Args:
            pairs: List of pair dicts with keys word_a, word_b,
                   phonemes_a, phonemes_b, label, similarity_score.
            vocab: PhonemeVocab instance for encoding.
        """
        self.pairs = pairs
        self.vocab = vocab

    def __len__(self) -> int:
        """Return total number of pairs."""
        return len(self.pairs)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
        """
        Return encoded tensors for one pair.

        Returns:
            phonemes_a:  LongTensor (max_length,)
            stress_a:    LongTensor (max_length,)
            phonemes_b:  LongTensor (max_length,)
            stress_b:    LongTensor (max_length,)
            label:       FloatTensor scalar — similarity_score [0.0, 1.0]
        """
        pair = self.pairs[idx]
        ph_a = pair.get("phonemes_a", [])
        ph_b = pair.get("phonemes_b", [])
        label = float(pair.get("similarity_score", 0.0))

        idx_a = self.vocab.encode(ph_a)
        str_a = self.vocab.encode_stress(ph_a)
        idx_b = self.vocab.encode(ph_b)
        str_b = self.vocab.encode_stress(ph_b)

        return idx_a, str_a, idx_b, str_b, torch.tensor(label, dtype=torch.float32)


